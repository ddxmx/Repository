package com.test.day03;

/**
 * 比较运算符
 */
public class CompareOperDemo {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 4;
        System.out.println(num1 > num2); //true
        System.out.println(num1 >= num2); //true
        System.out.println(num1 < num2); //false
        System.out.println(num1 <= num2); //false
        System.out.println(num1 == num2); //false
        System.out.println(num1 != num2); //true
    }
}
